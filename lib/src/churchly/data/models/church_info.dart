class ChurchInfo {
  final String branchName;
  final String address;
  final String pastorName;
  final String pastorNumber;
  final String treasurerName;
  final String treasurerNumber;
  final String accountMonth;
  final String accountYear;

  ChurchInfo({
    required this.branchName,
    required this.address,
    required this.pastorName,
    required this.pastorNumber,
    required this.treasurerName,
    required this.treasurerNumber,
    required this.accountMonth,
    required this.accountYear,
  });
}
